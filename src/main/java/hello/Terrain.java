package hello;

public class Terrain{
    private int X;
    private int Y;

    public Terrain(int X, int Y){
        this.X = X;
        this.Y = Y;
    }

	public int getX() {
		return X;
	}

	public void setX(int x) {
		this.X = x;
	}

	public int getY() {
		return Y;
	}

	public void setY(int y) {
		this.Y = y;
	}
    

}